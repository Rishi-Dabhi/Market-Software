    private void jLoginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jLoginButtonActionPerformed
        // TODO add your handling code here        
        User user = new User();
        if(user.isUser(jLoginEmailTextField.getText(), String.valueOf(jLoginPasswordField.getPassword()),
            String.valueOf(jLoginComboBox.getSelectedItem()))){
            if(String.valueOf(jLoginComboBox.getSelectedItem()) == "Owner"){
                jMainTabbedPane.addTab("Add Product", addproduct);
                jMainTabbedPane.addTab("Display Product", displayproduct);
                jMainTabbedPane.addTab("Basket", basketTab);
                jMainTabbedPane.addTab("Re-Order",reorder);
                jMainTabbedPane.addTab("Add Supplier", addsupplier);
                jMainTabbedPane.setSelectedIndex(1);
            }
            else if(String.valueOf(jLoginComboBox.getSelectedItem()) == "Customer"){
                jMainTabbedPane.addTab("Display Product", displayproduct);
                jMainTabbedPane.addTab("Basket", basketTab);
                jMainTabbedPane.setSelectedIndex(1);
            }
            else if(String.valueOf(jLoginComboBox.getSelectedItem()) == "Supplier"){
                jMainTabbedPane.addTab("Show Orders", showorders);
                jMainTabbedPane.setSelectedIndex(1);
            }
        }
        else{
            JOptionPane.showMessageDialog(this,"No User Found");
        }

        ArrayList<String> name = new ArrayList<String>();
        name = nameList.name();
        jDisplayProductComboBox.removeAllItems();
        jDisplayProductComboBox.addItem("Select Item");

        jSelectProductComboBox.removeAllItems();
        jSelectProductComboBox.addItem("Select Item");

        for(int i=0;i<name.size();i++){
            jDisplayProductComboBox.addItem(name.get(i));
            jSelectProductComboBox.addItem(name.get(i));
        }

        ArrayList<String> suppliername = new ArrayList<String>();
        suppliername = suppliernameList.name();
        jSelectSupplierComboBox.removeAllItems();
        jSelectSupplierComboBox.addItem("Select Supplier");
        for(int i=0;i<suppliername.size();i++){
            jSelectSupplierComboBox.addItem(suppliername.get(i));
        }
        
        wishList = new ProductList();
        
    }//GEN-LAST:event_jLoginButtonActionPerformed

